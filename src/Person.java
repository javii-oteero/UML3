public class Person {
    private String name;
    private int phoneNumber;
    private String email;
    private Adress adress;
    private AdressBook adressBook;

    public Person(String name, int phoneNumber, String email, Adress adress, AdressBook adressBook){
        this.name = name;
        this.phoneNumber = phoneNumber;
        this.email = email;
        this.adress = adress;
        this.adressBook = adressBook;
    }

}
